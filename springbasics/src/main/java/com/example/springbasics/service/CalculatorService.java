package com.example.springbasics.service;

public class CalculatorService {
    public int add(int x, int y) {
        return x + y;
    }

    public int sub(int x, int y) {
        return x - y;
    }

    public int mul(int x, int y) {
        return x * y;
    }

    public float div(int x, int y) {
        return (float) x / (float) y;
    }
}
